def is_even(number: int) -> bool:
    """Mengembalikan True jika number genap, False jika ganjil."""
    return number % 2 == 0


def is_odd(number: int) -> bool:
    """Mengembalikan True jika number ganjil, False jika genap."""
    return number % 2 != 0


